<div class=" col s12 m4 l8" >

    Au moment de la disparition de la MJC de Damparis, en 1997, la gestion d’Horizon Vertical a été reprise par les adhérents du club. Depuis, Horizon Vertical, affilié à la FFME (Fédération Française de Montagne et d’Escalade) pour des raisons d’assurance, reste néanmoins autonome.
    <br>
    Le club fonctionne exclusivement grâce au bénévolat.
<br><br>
    Il comporte, chaque année, environ 70 licenciés (filles et garçons) dont 50 jeunes pour 20 adultes. Une quinzaine de ces adultes (dont 1 est diplômé) endosse la fonction d’éducateur afin d’encadrer au mieux les entraînements des jeunes le mardi soir. Par cela, on peut constater à quel point le club est orienté vers les jeunes.
<br><br>
    A noter que, pour la région, notre club est un des rares à accueillir des enfants à partir de 8 ans. Cela nous place dans les clubs de tête quant au nombre de licenciés.
<br><br>
    Nous sommes hébergés par la commune de Damparis dans son gymnase Auguste Delaune. L’escalade peut ainsi se pratiquer sur 2 structures artificielles : un mur de 80 m2 dans la grande salle et un bloc de 120 m2 dans la salle des agrès. Quant à l’évolution sur site naturel, les falaises de Rochefort-sur-Nenon sont le plus souvent utilisées, y compris avec les jeunes à la belle saison. Bien d’autres sites plus réputés peuvent également être convoités, notamment les fameuses calanques de Marseille à Cassis.
<br><br>
    Concernant les compétitions, seuls les jeunes sont concernés. Il faut dire que la philosophie du club n’est pas concentrée sur la compétition. Il ne s’agit que d’une motivation supplémentaire pour ceux qui en manifestent l’envie. Néanmoins, nos jeunes grimpeurs trustent souvent les premières places des coupes du Jura et parfaois même de Franche-Comté (Doubs, Haute-Saône et Territoire de Belfort). Il s’agit tout de même d’un motif de satisfaction pour le club.
<br><br>
    Ce sport, à priori à caractère individuel, est en fait beaucoup plus collectif qu’on ne peut l’imaginer. Il permet un développement physique et technique, mais aussi psychologique. Il véhicule toutes les valeurs sportives, non seulement physiques, mais également humaines dont le respect, la tolérance, l’humilité, la confiance, etc … 
<br><br>
    Quant aux temps forts traditionnels au club, ce sont, principalement, la compétition comptant pour les coupes du Jura et de Franche-comté que nous organisons pour les catégories Ecureuils et Poussins, ainsi que la sortie famille à Ranchot en fin de saison.

